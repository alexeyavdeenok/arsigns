package com.example.domain.api

/**
 * Интерфейс взаимодействия с модулем озвучки текста (:feature-voice).
 * Отвечает за генерацию речи из текста (Text-to-Speech) для аудио-уведомлений.
 */
interface VoiceLayerApi {

    /**
     * Произнести переданный текст вслух.
     * Сюда будет передаваться строка "voiceText" из метаданных знака.
     * * Реализация этого метода внутри :feature-voice должна самостоятельно проверять,
     * включен ли звук в настройках (AppSettings.isVoiceAlertsEnabled).
     *
     * @param text Строка для прочтения (например, "Впереди ограничение скорости пятьдесят")
     */
    fun speak(text: String)

    fun speakPriority(ttsText: String)

    /**
     * Немедленно прервать текущую речь.
     * Полезно, если знак уже пропал из виду или ситуация резко изменилась.
     */
    fun stop()

    /**
     * Освободить ресурсы движка TTS (TextToSpeech) при закрытии приложения.
     */
    fun shutdown()
}